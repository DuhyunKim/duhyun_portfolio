package com.tie.document.model;

public class DocumentDto {
	private int docId;
	private int empId;
	private int docTypeId;
	private int stateSeq;
	private int deptId;
	private String docDDate;
	private String docName;
	private String docContent;
	private String docDate;
	
	public int getDocId() {
		return docId;
	}
	public void setDocId(int docId) {
		this.docId = docId;
	}
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public int getDocTypeId() {
		return docTypeId;
	}
	public void setDocTypeId(int docTypeId) {
		this.docTypeId = docTypeId;
	}
	public int getStateSeq() {
		return stateSeq;
	}
	public void setStateSeq(int stateSeq) {
		this.stateSeq = stateSeq;
	}
	public int getDeptId() {
		return deptId;
	}
	public void setDeptId(int deptId) {
		this.deptId = deptId;
	}
	public String getDocDDate() {
		return docDDate;
	}
	public void setDocDDate(String docDDate) {
		this.docDDate = docDDate;
	}
	public String getDocName() {
		return docName;
	}
	public void setDocName(String docName) {
		this.docName = docName;
	}
	public String getDocContent() {
		return docContent;
	}
	public void setDocContent(String docContent) {
		this.docContent = docContent;
	}
	public String getDocDate() {
		return docDate;
	}
	public void setDocDate(String docDate) {
		this.docDate = docDate;
	}
	
}

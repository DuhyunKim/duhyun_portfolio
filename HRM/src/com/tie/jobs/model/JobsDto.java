package com.tie.jobs.model;

public class JobsDto {
	private int jobId;
	private String jobName;
	private String jobRank;
	private int jobLife;
	private int jobcaree;
	
	public int getJobId() {
		return jobId;
	}
	public void setJobId(int jobId) {
		this.jobId = jobId;
	}
	public String getJobName() {
		return jobName;
	}
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}
	public String getJobRank() {
		return jobRank;
	}
	public void setJobRank(String jobRank) {
		this.jobRank = jobRank;
	}
	public int getJobLife() {
		return jobLife;
	}
	public void setJobLife(int jobLife) {
		this.jobLife = jobLife;
	}
	public int getJobcaree() {
		return jobcaree;
	}
	public void setJobcaree(int jobcaree) {
		this.jobcaree = jobcaree;
	}
	
	
}

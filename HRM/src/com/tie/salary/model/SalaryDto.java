package com.tie.salary.model;

public class SalaryDto {

	int empId;
	int salaryMoney;
	int salaryIncentive;
	int salaryBonus;
	int salaryBasic;
	int salaryAdd;
	
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public int getSalaryMoney() {
		return salaryMoney;
	}
	public void setSalaryMoney(int salaryMoney) {
		this.salaryMoney = salaryMoney;
	}
	public int getSalaryIncentive() {
		return salaryIncentive;
	}
	public void setSalaryIncentive(int salaryIncentive) {
		this.salaryIncentive = salaryIncentive;
	}
	public int getSalaryBonus() {
		return salaryBonus;
	}
	public void setSalaryBonus(int salaryBonus) {
		this.salaryBonus = salaryBonus;
	}
	public int getSalaryBasic() {
		return salaryBasic;
	}
	public void setSalaryBasic(int salaryBasic) {
		this.salaryBasic = salaryBasic;
	}
	public int getSalaryAdd() {
		return salaryAdd;
	}
	public void setSalaryAdd(int salaryAdd) {
		this.salaryAdd = salaryAdd;
	}
	
}
